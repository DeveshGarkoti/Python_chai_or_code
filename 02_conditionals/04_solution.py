# Fruit Ripeness Checker
fruit = "Banana"
fruit_color = "Green"
fruit_ripeness = ""

if fruit == "Banana":
  if fruit_color == "Green":
    fruit_ripeness = "Unripe"
  elif fruit_color == "Yellow":
    fruit_ripeness = "Ripe"
  elif fruit_color == "Brown":
    fruit_ripeness = "Overripe"


print("fruit ripeness is ",fruit_ripeness)